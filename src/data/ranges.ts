export const RANGES_BASE: Record<'UTG' | 'MP' | 'HJ' | 'CO' | 'BTN', Range> = {
  UTG: new Set(UTG_HANDS),
  MP: new Set(MP_HANDS),
  HJ: new Set(HJ_HANDS),
  CO: new Set(CO_HANDS),
  BTN: new Set(BTN_HANDS),
}

// PDF charts use two highlight colors. Keep this mapping for visual parity.
export const RANGES_SECONDARY_BASE: Record<'UTG' | 'MP' | 'HJ' | 'CO' | 'BTN', Range> = {
  UTG: new Set(['55', '44', '33', '22']),
  MP: new Set(['33', '22']),
  HJ: new Set(),
  CO: new Set(),
  BTN: new Set(),
}

export function getRangeForPosition(position: TrainablePosition): Range {
  if (position === 'UTG+1') return RANGES_BASE.UTG
  if (position === 'MP+1') return RANGES_BASE.HJ
  return RANGES_BASE[position]
}

export function getSecondaryRangeForPosition(position: TrainablePosition): Range {
  if (position === 'UTG+1') return RANGES_SECONDARY_BASE.UTG
  if (position === 'MP+1') return RANGES_SECONDARY_BASE.HJ
  return RANGES_SECONDARY_BASE[position]
}

export function normalizeHandKey(handKey: string): string {
  if (handKey.length === 2) {
    return handKey
  }
  if (handKey.endsWith('s') || handKey.endsWith('o')) {
    return handKey
  }
  return `${handKey}o`
}

export function isHandInRange(position: TrainablePosition, handKey: string): boolean {
  const range = getRangeForPosition(position)
  return range.has(normalizeHandKey(handKey))
}
